#include <stdio.h>

int main() {
    // Fazendo comentário de uma linha.
    printf("Para fazer comentários de uma linha, basta utilizar o caractere '//' duas vezes.\n");

    /*
     * Dessa forma, fazemos comentário
     * utilizando mais de uma linha.
    */
    printf("Para fazer comentários de múltiplas linhas, utiliza-se '/*' seguido de '*/'.\n");

    return 0;
}