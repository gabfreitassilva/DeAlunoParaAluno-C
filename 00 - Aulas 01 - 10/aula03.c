#include <stdio.h>

int main() {
    // Para declarar uma variável, deve-se especificar primeiro o seu tipo e
    // após o tipo escrever o nome que você deseja para sua variável.
    int minhaIdade; // Declaração.
    minhaIdade = 23; // Atribuição do valor.

    printf("Minha idade é: %d.\n", minhaIdade);
    // Os tipos de dados para variáveis existentes no C são:
    // int double float char

    return 0;
}