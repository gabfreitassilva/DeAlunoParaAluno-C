#include <stdio.h>

int main() {
    int inteiro = 10; // declaração e atribuição de número inteiro
    float decimal = 4.78; // declaração e atribuição de número decimal
    char caractere = 'k'; // declaração e atribuição de variável char, para caracteres únicos ou cadeia de caracteres utilizando vetores

    printf("Valor inteiro: %d.\nValor decimal: %f.\nÚnico caractere: %c.\n", inteiro, decimal, caractere);

    return 0;
}