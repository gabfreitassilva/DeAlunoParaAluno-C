#include <stdio.h>

int main() {
    int minhaIdade; // Declaração da variável
    minhaIdade = 23; // Atribuição de valor a variável
    int gatoIdade = 7;  // Declaração juntamente com atribuição na mesma linha

    // Print com múltiplas variáveis e exibição em mais de uma linha
    printf("Minha idade é: %d.\nMeu gato tem idade de: %d.\n", minhaIdade, gatoIdade);

    return 0;
}